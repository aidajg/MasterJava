package com.curso;

public class Persona {
	public String nombre;
	public String apellido;
	public String edad;
	public String dni;
	public String puesto;
	
	public Persona(String nombre, String apellido, String edad, String dni, String puesto) {
		super();
		this.nombre = nombre;
		this.apellido = apellido;
		this.edad = edad;
		this.dni = dni;
		this.puesto = puesto;
	}
	
	
}
